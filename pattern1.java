public class pattern1 {
    public static void main(String args[]) {
        for(int rc=1;rc<=3;rc++){
            for(int j=2;j>=rc;j--){
                System.out.print(" ");
            }
            for(int k=1;k<=rc;k++)
            {
            System.out.print("*");
            }
            System.out.println(" ");
            
        }

        
    }
}
